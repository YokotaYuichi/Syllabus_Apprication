package javacode;

public class Student {
	private String name;
	private String id;
	private String mailAddress;

	public void setName(String n) {
		name = n;
	}

	public String getName() {
		return name;
	}

	public void setId(String i) {
		id = i;
	}

	public String getId() {
		return id;
	}

	public void setMailAddress(String m) {
		mailAddress = m;
	}

	public String getMailAddress() {
		return mailAddress;
	}
}
